require 5.004;
use strict;
use ExtUtils::MakeMaker;
use File::Spec ();

{
  my $net_tests = prompt(
    "Would you like to run actual network tests?\n" .
    "They take a long time and may hang indefinitely (y/n):",
    'n'
  );
  my $file = File::Spec->catfile(qw(t .net_tests));
  if( $net_tests =~ /^y/i ){
    open(my $fh, '>', $file); # touch
  }
  else {
    unlink $file if -e $file;
  }
}

WriteMakefile(
    'NAME'	    => 'LWP::UserAgent::Determined',
    'VERSION_FROM'  => 'lib/LWP/UserAgent/Determined.pm',
    'ABSTRACT_FROM' => 'lib/LWP/UserAgent/Determined.pm',
    'PREREQ_PM'     => {
        'LWP'                 => 0,
    },
    'dist'         => { COMPRESS => 'gzip -6f', SUFFIX => 'gz', },
);

package MY;

sub libscan
{ # Determine things that should *not* be installed
    my($self, $path) = @_;
    return '' if $path =~ m/~/;
    $path;
}

